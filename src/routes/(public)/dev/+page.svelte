<h1>Dev</h1>

<p>Start the development server.</p>

<h3>Syntax</h3>

<pre><code>{`$ vela dev`}</code></pre>

<p></p>

<p>
	This will start the development server and the database. The server will be available at <code
		>http://localhost:5173</code
	>
	and the admin interface at
	<code>http://localhost:5173/admin</code>.
</p>
