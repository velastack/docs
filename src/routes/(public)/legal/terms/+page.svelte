<h1>Terms</h1>

<p>
	Vela includes a terms of service generator to create a demo terms of service for your application.
</p>

<h3>Syntax</h3>
<pre><code>{`$ vela legal terms`}</code></pre>

<p></p>

<p>
	This will start a wizard to guide you through the process of creating a demo terms of service for
	your application. The generated terms of service is a placeholder and not a legal document. Seek
	legal advice for your specific use case.
</p>
