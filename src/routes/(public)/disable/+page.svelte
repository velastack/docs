<h1>Disable</h1>

<p>The disable command is used to disable features enabled by the enable command.</p>

<h3>Syntax</h3>
<pre><code>{`$ vela disable <feature>`}</code></pre>

<p></p>

<p>The following features can be disabled:</p>

<ul>
	<li><code>auth</code> - Disable auth</li>
	<li><code>api</code> - Disable API</li>
	<li><code>api-keys</code> - Disable API keys</li>
	<li><code>s3</code> - Disable S3</li>
	<li><code>smtp</code> - Disable SMTP</li>
	<li><code>i18n</code> - Disable internationalization</li>
	<li><code>teams</code> - Disable teams</li>
	<li><code>payments</code> - Disable payments</li>
	<li><code>subscriptions</code> - Disable subscriptions</li>
	<li><code>cms</code> - Disable the content management system</li>
	<li><code>workflow</code> - Disable Workflow</li>
</ul>
