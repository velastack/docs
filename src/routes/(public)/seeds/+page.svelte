<h1>Seeds</h1>

<p>
	Seed data is used to populate the database with initial data that is essential for the application
	to function. They are stored in <code>data/seeds</code> directory.
</p>

<h3>Syntax</h3>
<pre><code>{`$ vela seeds <action>`}</code></pre>

<p></p>

<p class="mb-0!">The action can be one of the following:</p>

<ul>
	<li><code>load</code> - Load seed data from the database</li>
	<li><code>save</code> - Save seed data to the database</li>
	<li><code>clear</code> - Clear all seed data from the database</li>
</ul>
