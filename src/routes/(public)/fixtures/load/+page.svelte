<h1>Load</h1>

<p>Load fixture data into the database. Run automatically for integration tests.</p>

<h3>Syntax</h3>
<pre><code>{`$ vela fixtures load`}</code></pre>

<p></p>

<p>
	This will load fixture data into the database from the <code>data/fixtures</code> directory. Fixtures
	are loaded in alphabetical order and prefixed with a number to ensure deterministic loading order.
</p>
